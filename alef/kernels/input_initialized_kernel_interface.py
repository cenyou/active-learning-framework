import numpy as np
from typing import Tuple, Optional
from abc import ABC, abstractmethod


class InputInitializedKernelInterface(ABC):
    """
    Interface for kernels for which the parameters are initialized by the input dataset
    """

    @abstractmethod
    def initialize_parameters(self, x_data, y_data):
        raise NotImplementedError
